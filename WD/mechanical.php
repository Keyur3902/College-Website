<ul class="vision_mission_list" style = "margin : 64px;">

				<li><b>Engineering knowledge:</b> Apply the knowledge of
					mathematics, science, engineering fundamentals, and an engineering
					specialization to the solution of complex engineering problems.</li>
				<br>

				<li><b>Problem analysis:</b> Identify, formulate, review
					research literature, and analyze complex engineering problems
					reaching substantiated conclusions using first principles of
					mathematics, natural sciences, and engineering sciences</li>
				<br>

				<li><b>Design/development of solutions:</b> Design solutions
					for complex engineering problems and design system components or
					processes that meet the specified needs with appropriate
					consideration for the public health and safety, and the cultural,
					societal, and environmental considerations.</li>
				<br>

				<li><b>Conduct investigations of complex problems:</b> Use
					research-based knowledge and research methods including design of
					experiments, analysis and interpretation of data, and synthesis of
					the information to provide valid conclusions.</li>
				<br>

				<li><b>Modern tool usage:</b> Create, select, and apply
					appropriate techniques, resources, and modern engineering and IT
					tools including prediction and modeling to complex engineering
					activities with an understanding of the limitations.</li>
				<br>

				<li><b>The engineer and society:</b> Apply reasoning informed
					by the contextual knowledge to assess societal, health, safety,
					legal and cultural issues and the consequent responsibilities
					relevant to the professional engineering practice.</li>
				<br>

				<li><b>Environment and sustainability:</b> Understand the
					impact of the professional engineering solutions in societal and
					environmental contexts, and demonstrate the knowledge of, and need
					for sustainable development.</li>
				<br>

				<li><b>Ethics:</b> Apply ethical principles and commit to
					professional ethics and responsibilities and norms of the
					engineering practice.</li>
				<br>

				<li><b>Individual and team work:</b> Function effectively as an
					individual, and as a member or leader in diverse teams, and in
					multidisciplinary settings.</li>
				<br>

				<li><b>Communication:</b> Communicate effectively on complex
					engineering activities with the engineering community and with
					society at large, such as, being able to comprehend and write
					effective reports and design documentation, make effective
					presentations, and give and receive clear instructions.</li>
				<br>

				<li><b>Project management and finance:</b> Demonstrate
					knowledge and understanding of the engineering and management
					principles and apply these to one's own work, as a member and
					leader in a team, to manage projects and in multidisciplinary
					environments.</li>
				<br>

				<li><b>Life-long learning:</b> Recognize the need for, and have
					the preparation and ability to engage in independent and life-long
					learning in the broadest context of technological change.</li>
				<br>
			</ul>


